  /**
   * Valida si (lat,lng) está dentro del polígono.
   */
  static isInsideCoverage(lat: number, lng: number): boolean {
    let inside = false;
    const len = this.POLYGON.length;

    for (let i = 0, j = len - 1; i < len; j = i++) {
      const [x1, y1] = this.POLYGON[i];
      const [x2, y2] = this.POLYGON[j];

      if (
        (y1 > lat) !== (y2 > lat) &&
        lng < ((x2 - x1) * (lat - y1)) / (y2 - y1) + x1
      ) {
        inside = !inside;
      }
    }

    return inside;
  }
